public class Example_06 {
    public static void main(String[] args) {
        System.out.printf("byte 형의 크기\t ==> %d\n", Byte.SIZE);  //래퍼 클래스의 size필드를 통헤 데이터형의 크기를 비트 단위로 출력한다.
        System.out.printf("short 형의 크기\t ==> %d\n", Short.SIZE);
        System.out.printf("int 형의 크기\t ==> %d\n", Integer.SIZE);
        System.out.printf("long 형의 크기\t ==> %d\n", Long.SIZE);
        System.out.printf("float 형의 크기\t ==> %d\n", Float.SIZE);
        System.out.printf("double 형의 크기 ==> %d\n", Double.SIZE);
        System.out.printf("char 형의 크기\t ==> %d\n", Character.SIZE);               
    }
}
